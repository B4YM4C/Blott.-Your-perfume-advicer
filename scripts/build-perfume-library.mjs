//   In  = Intensity  (-10 skin scent ←→ +10 powerhouse)
//   Fo  = Formality  (-10 casual ←→ +10 office/formal)
//   Ti  = Time       (+10 day-only ←→ -10 night-only)
//   Mod = Modern     (-10 vintage ←→ +10 ultra-modern)
//   Sx  = Sexy       (-10 innocent ←→ +10 seductive)
//   Lu  = Luxury     (-10 cheap ←→ +10 luxe)
//   Pl  = Playful    (-10 serious ←→ +10 playful)
